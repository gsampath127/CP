DECLARE @totalRowCount int,
	@RowCount int
	
SELECT @totalRowCount = COUNT(*) from @FlaggedCusips
	
    
   
    Set @RowCount = 1
    
    DECLARE @CurrentCUSIP  nvarchar(10)
    
    --SET @totalRowCount= 2
    
    DECLARE @IsProcessed bit
    
    DECLARE @ZipFileName nvarchar(255),
		   @IsExported bit,
		   @ExportedDate datetime,
		   @IsAPF bit,
		   @APFReceivedDate datetime,
		   @IsOPF bit,
		   @OPFReceivedDate datetime,
		   @BCSDocUpdateSupplementsSlinkID int
    
    
    WHILE @RowCount <= @totalRowCount
	BEGIN
	
		SET @IsProcessed = null
		
		SELECT @CurrentCUSIP = CUSIP from @FlaggedCusips 
		  WHERE ID = @RowCount
		  
		  SELECT @IsProcessed = IsProcessed 
		    FROM BCSDocUpdate
		    WHERE IsRemoved=0 and CUSIP = @CurrentCUSIP
		
		SET @BCSDocUpdateSupplementsSlinkID= null
		
		 IF @IsProcessed = 1
		 BEGIN
		 
			SELECT @ZipFileName =ZipFileName,@IsExported=IsExported,
					@ExportedDate=ExportedDate,@IsAPF=IsAPF,
					@APFReceivedDate=APFReceivedDate,@IsOPF=IsOPF,@OPFReceivedDate=OPFReceivedDate
				   FROM BCSDocUpdateGIMSlink
				   INNER JOIN BCSDocUpdate on BCSDocUpdateGIMSlink.Docupdateid = BCSDocUpdate.BCSDocUpdateId
				   WHERE CUSIP = @CurrentCUSIP
				   AND CUSIP not in (SELECT CUSIP FROM BCSDocUpdateSupplements WHERE IsRemoved=0)
				   and IsRemoved=0
				   
			IF @ZipFileName is not null	   
		    BEGIN
				SELECT @BCSDocUpdateSupplementsSlinkID=BCSDocUpdateSupplementsSlinkID 
				 FROM BCSDocUpdateSupplementsSlink
				 WHERE ZipFileName = @ZipFileName
			  
				 IF @BCSDocUpdateSupplementsSlinkID is null
				 BEGIN
					 INSERT INTO BCSDocUpdateSupplementsSlink(ZipFileName,IsExported,ExportedDate,
															IsAPF,APFReceivedDate,IsOPF,OPFReceivedDate)
											VALUES(@ZipFileName,@IsExported,@ExportedDate,
												@IsAPF,@APFReceivedDate,@IsOPF,@OPFReceivedDate
												)	
												
					SET @BCSDocUpdateSupplementsSlinkID = @@IDENTITY
								 
				 END
		    END
		 END 
		 
		 INSERT INTO BCSDocUpdateSupplements([EdgarID]
      ,[Acc#]
      ,[PDFName]
      ,[DocumentType]
      ,[CUSIP]
      ,[TickerID]
      ,[EffectiveDate]
      ,[DocumentDate]
      ,[ProsID]
      ,[FundName]
      ,[ProsDocID]
      ,[IsFiled]
      ,[FiledDate]
      ,[FilingStatusAddedDate]
      ,[IsProcessed]
      ,[ProcessedDate]
      ,[PageCount]
      ,[PageSizeHeight]
      ,[PageSizeWidth]
      ,[BCSDocUpdateSupplementsSlinkID]
      ,[FormType]
      )
      SELECT DISTINCT [EdgarID]
      ,BCSDocUpdate.[Acc#]
      ,[PDFName]
      ,BCSDocUpdate.[DocumentType]
      ,[CUSIP]
      ,[TickerID]
      ,BCSDocUpdate.[EffectiveDate]
      ,BCSDocUpdate.[DocumentDate]
      ,[ProsID]
      ,[FundName]
      ,[ProsDocID]
      ,[IsFiled]
      ,[FiledDate]
      ,[FilingStatusAddedDate]
      ,[IsProcessed]
      ,[ProcessedDate]
      ,[PageCount]
      ,[PageSizeHeight]
      ,[PageSizeWidth]
      ,@BCSDocUpdateSupplementsSlinkID
      ,[FormType]
      FROM BCSDocUpdate
      INNER JOIN BCSDocUpdateSECDetails on BCSDocUpdate.BCSDocUpdateId = BCSDocUpdateSECDetails.DocUpdateID
      AND BCSDocUpdate.Acc# = BCSDocUpdateSECDetails.Acc#
      WHERE CUSIP = @CurrentCUSIP AND CUSIP not in (SELECT CUSIP FROM BCSDocUpdateSupplements) and IsRemoved=0
      
		 
		  
		  SET @RowCount =  @RowCount +1
		  
		  print @RowCount
	END
